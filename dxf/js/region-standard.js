/** 같은 글자수에서 1글자 이하만 다르면 같은 것으로 본다(단순오타, 시/군 단위 오기재 등) */
function _hammingWithinOne(a, b) {
  if (a.length !== b.length) return false;
  let diff = 0;
  for (let i = 0; i < a.length; i++) {
    if (a[i] !== b[i]) { diff++; if (diff > 1) return false; }
  }
  return true;
}

function _fuzzyFind(input, candidates) {
  const hits = candidates.filter(c => _hammingWithinOne(input, c));
  return hits.length === 1 ? hits[0] : null; // 여러 개가 동시에 걸리면 애매하므로 모달로 넘긴다
}

/** 광역자치단체 명칭을 표준 명칭으로. 못 찾으면 null. */
function normalizeProvince(raw) {
  const s = String(raw || '').trim();
  if (!s) return null;
  if (STANDARD_PROVINCES.includes(s)) return s;
  if (PROVINCE_ALIASES[s]) return PROVINCE_ALIASES[s];
  return _fuzzyFind(s, STANDARD_PROVINCES);
}

/**
 * 기초자치단체 명칭을 표준 명칭으로. province가 이미 정규화돼 있으면 그 안에서만 찾고,
 * 없으면 전체 기초자치단체 목록에서 찾는다(이름이 겹치는 시/군이 있을 수 있어 province가
 * 있을 때보다 부정확할 수 있음). 못 찾으면 null.
 */
function normalizeCity(raw, province) {
  const s = String(raw || '').trim();
  if (!s) return null;

  const pool = province && STANDARD_CITIES_BY_PROVINCE[province]
    ? STANDARD_CITIES_BY_PROVINCE[province]
    : Object.values(STANDARD_CITIES_BY_PROVINCE).flat();

  if (pool.includes(s)) return s;
  return _fuzzyFind(s, pool);
}
